#	include "libft.h"

int	ft_atoi(const char *str)
{
	int			i[3];

	i[0] = 0;
	i[1] = 0;
	i[2] = 0;
	while (str[i[0]] == ' ' || str[i[0]] == '\t' || str[i[0]] == '\n' \
	 || str[i[0]] == '\r' || str[i[0]] == '\v' || str[i[0]] == '\f')
		i[0]++;
	if (str[i[0]] == '-' || str[i[0]] == '+')
	{
		if (str[i[0]] == '-')
			i[2]++;
		i[0]++;
	}
	if (i[2] && ft_strncmp(&str[i[0]], "2147483648", 0))
		return (-2147483648);
	while (str[i[0]] >= '0' && str[i[0]] <= '9')
	{
		i[1] = i[1] * 10 + (str[i[0]] - 48);
		i[0]++;
	}
	if (i[2])
		return (-i[1]);
	return (i[1]);
}
